<Subheader class="text-tertiary">Exercises</Subheader>
<Paragraph class="text-tertiary">
	Design exercises that illustrate various ways this methodology of Chimeric Worlding can be applied
</Paragraph>
<Paragraph>
	<ol class="list-decimal text-tertiary pl-6">
		<li class="mb-2">
			Create your own library of personal symbols. assign each symbol with its own associated color,
			temperament, element, ruling planet, scent, musical genre, species of flower, etc. Which
			symbols are weak against other symbols? which symbols harmonize and strengthen each other?
			which symbols negate each other? which symbols dominate over others?
		</li>
		<li class="mb-2">
			Overlay multiple systems of knowledge, classification, and taxonomic schemes that govern your
			world or are most relevant to you. See how they interact. (For example, map the 5 elements in
			Chinese philosophy, then overlay a soil classification chart over it, then map it all onto a
			political compass).
		</li>
		<li class="mb-2">
			Numbers are archetypal and often have many levels of significance. Ex:
			<div class="pl-4 text-sm">
				<p>1: unity and wholeness</p>
				<p>2: duality, binaries, dialectics</p>
				<p>3: balance, trinities - [ego, id, superego], [father, son, holy spirit]</p>
				<p>
					4: stability, order, materiality, universality, four elements, four corners, four humours
				</p>
				<p>5: five platonic solids, five fingers,five senses,five elements, quintessence</p>
				<p>6: perfection, six days of creation, six strings on a guitar</p>
				<p>
					7: luck, seven virtues, seven deadly sins, seven days a week, seven treasures of buddhism
				</p>
			</div>
			Choose a number and think of as many taxonomies that come in that number. Visually map them all
			together.
		</li>
		<li class="mb-2">
			Describe and map out your own imagined system of gender. Then gender all of the objects in
			your room with this new system, and visually represent them.
		</li>
		<li class="mb-2">
			Arrange your next poster based on sacred geometries, either pulled from historical/cultural
			references or created by you.
		</li>
		<li class="mb-2">
			Create your own magic system, as if you were a sci-fi or fantasy writer, but then represent it
			all visually. There are writing worksheets available online to help you get started.
		</li>
		<li class="mb-2">
			Make a list of your favorite words or phrases, and then map/graph out how all of those words
			relate to each other. Make oppositions and alliances. Experiment with how these words would
			map onto other, existing structures. (Ex: which words are phlegmatic, which are choleric,
			melancholic, and sanguine? Or, which words would you associate with each tarot card in the
			major arcana? Or, map the words onto a chart that classifies fragrances: which words smell
			floral to you? which smell woody? which are musky? Or, which words are birds? which are
			reptiles? which are mammals? which are bugs? which are mushrooms?)
		</li>
		<li class="mb-2">
			Draw a symbol, icon, or character, or letterform inspired by each element in some taxonomy
			(for example: draw a character or symbol or even make an entire poster for each domain of the
			9 muses in greek mythology: epic poetry, history, love poetry, music, tragedy, hymns, dance,
			comedy, and astronomy).
		</li>
		<li class="mb-2">
			Find any number of existing graphs, charts, and diagrams. Remove all the text and relabel
			everything.
		</li>
		<li class="mb-2">
			Collect found words or phrases (can come from anywhere â€” snippets of dialogue, text messages,
			lines from poems). Use them as the captions for images. Be mindful of the connections you are
			making. Arrange these images (and their captions) hierarchically/systematically somehow, onto
			a poster or publication or website (or some other format?)
		</li>
		<li class="mb-2">
			Come up with your microgenre. Coin your own -punk, -wave or -core. Imagine the world that this
			microgenre describes. Flesh out a graphic language for this world.
		</li>
		<li class="mb-2">
			Draw a custom symbol, icon, illustration, or logotype for the title of every song on your
			favorite album.
		</li>
		<li class="mb-2">
			Create a character and insert them somehow into everything you make for the next month. Over
			time, give them some friends. Build out their world.
		</li>
	</ol>
</Paragraph>
